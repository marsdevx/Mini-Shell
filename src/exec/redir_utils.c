#include "../init/header.h"

int	handle_input_redirect(const char *filename)
{
	int	fd;

	fd = open(filename, O_RDONLY);
	if (fd < 0)
	{
		write_error_with_arg("bash: ", filename, ": ");
		write_error(strerror(errno));
		write_error("\n");
		return (-1);
	}
	if (dup2(fd, STDIN_FILENO) < 0)
	{
		perror("dup2");
		close(fd);
		return (-1);
	}
	close(fd);
	return (0);
}

